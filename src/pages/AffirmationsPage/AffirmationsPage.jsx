const fonts = {
  serif: [
    "Cormorant",
    "Gabriela",
    "Podkova",
    "Zen Antique Soft",
    "IBM Plex Serif",
  ],
  "sans-serif": [
    "Pattaya",
    "Overpass",
    "Balsamiq Sans",
    "LXGW Marker Gothic",
    "Kelly Slab",
  ],
  "system-ui": ["Comic Relief"],
  cursive: ["Pacifico", "Caveat", "Marck Script", "Pangolin", "Shantell Sans"],
};

const fontFamilies = Object.keys(fonts);

function getRandomAffirmation() {
  const randomIndex = Math.floor(Math.random() * affirmations.length);
  return affirmations[randomIndex];
}

function getRandomFont() {
  const randomFamily =
    fontFamilies[Math.floor(Math.random() * fontFamilies.length)];
  console.log(randomFamily);
  const randomIndex = Math.floor(Math.random() * fonts[randomFamily].length);
  return [randomFamily, fonts[randomFamily][randomIndex]];
}

function AffirmationsPage(props) {
  const [affirmation, setAffirmation] = useState(getRandomAffirmation);
  const [font, setFont] = useState(getRandomFont);

  const handleUpdate = () => {
    let nextAffirmation;
    let nextFont;
    do {
      nextAffirmation = getRandomAffirmation();
      nextFont = getRandomFont();
    } while (nextAffirmation === affirmation && affirmations.length > 1);
    setAffirmation(nextAffirmation);
    setFont(nextFont);
  };

  return (
    <>
      <NavBar />
      <div className="content affirmations-container">
        <div
          className="affirmation-text"
          style={{ fontFamily: `${font[1]}, ${font[0]}` }}
        >
          {affirmation}
        </div>
        <button className="refresh-button" onClick={handleUpdate}>
          <i className="bi bi-arrow-clockwise"></i> Обновить
        </button>
      </div>
    </>
  );
}

export default AffirmationsPage;
